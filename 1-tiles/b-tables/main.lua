function love.draw()

  local quad

  for y,row in ipairs(TileTable) do
    for x,tileKey in ipairs(row) do
      if tileKey == 0 then
        quad = Quads.grass
      else
        quad = Quads.box
      end
      
      love.graphics.drawq(Tileset, quad, (x-1)*TileW, (y-1)*TileH)

    end
  end
end
